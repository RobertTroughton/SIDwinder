//; Number of available bar styles
.const NUM_BAR_STYLES = 8

//; Size per style:
//; - Water reflection: 30 chars (10 main + 10 ref1 + 10 ref2) = 240 bytes
//; - Mirror: 20 chars (10 main + 10 mirror) = 160 bytes
.const BAR_STYLE_SIZE_WATER = 240
.const BAR_STYLE_SIZE_MIRROR = 160

//; BarStyle variable location in data block (offset $70 from DATA_ADDRESS)
.var BarStyle = DATA_ADDRESS + $70

//; =============================================================================
//; BAR STYLE COPY ROUTINE - WATER REFLECTION VERSION
//; Use this for RaistlinBars and RaistlinBarsWithLogo
//; =============================================================================

CopyBarStyleWater:
    lda BarStyle
    cmp #NUM_BAR_STYLES
    bcc !validStyle+
    lda #$00
!validStyle:
    tax
    lda BarStylesWaterLo, x
    sta !copyLoop+ + 1
    lda BarStylesWaterHi, x
    sta !copyLoop+ + 2

    ldx #$00
!copyLoop:
    lda BarStyleDataWater, x
    sta CHARSET_ADDRESS + (224 * 8), x
    inx
    cpx #BAR_STYLE_SIZE_WATER
    bne !copyLoop-

    rts

//; =============================================================================
//; BAR STYLE COPY ROUTINE - MIRROR VERSION
//; Use this for RaistlinMirrorBars and RaistlinMirrorBarsWithLogo
//; =============================================================================

CopyBarStyleMirror:
    lda BarStyle
    cmp #NUM_BAR_STYLES
    bcc !validStyle+
    lda #$00
!validStyle:
    tax
    lda BarStylesMirrorLo, x
    sta !copyLoop+ + 1
    lda BarStylesMirrorHi, x
    sta !copyLoop+ + 2

    ldx #$00
!copyLoop:
    lda BarStyleDataMirror, x
    sta CHARSET_ADDRESS + (224 * 8), x
    inx
    cpx #BAR_STYLE_SIZE_MIRROR
    bne !copyLoop-

    rts

//; =============================================================================
//; LOOKUP TABLES - WATER REFLECTION STYLES
//; =============================================================================

BarStylesWaterLo:   .fill NUM_BAR_STYLES, <(BarStyleDataWater + (i * BAR_STYLE_SIZE_WATER))
BarStylesWaterHi:   .fill NUM_BAR_STYLES, >(BarStyleDataWater + (i * BAR_STYLE_SIZE_WATER))

//; =============================================================================
//; LOOKUP TABLES - MIRROR STYLES
//; =============================================================================

BarStylesMirrorLo:  .fill NUM_BAR_STYLES, <(BarStyleDataMirror + (i * BAR_STYLE_SIZE_MIRROR))
BarStylesMirrorHi:  .fill NUM_BAR_STYLES, >(BarStyleDataMirror + (i * BAR_STYLE_SIZE_MIRROR))

