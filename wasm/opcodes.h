// `opcodeTable[op]` returns the mnemonic, addressing mode, instruction size
// (bytes), base cycle count, and an `illegal` flag for every 8-bit opcode.
// Cycle counts are the base count only - page-crossing penalties for some
// addressing modes and branch-taken penalties are added by the executor.
